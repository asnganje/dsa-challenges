### **14. Coin Change Problem**
   - **Problem**: Given a set of coin denominations, find the minimum number of coins needed to make a certain amount.
   - **Example**:  
     Input: `coins = [1, 2, 5], amount = 11`  
     Output: `3`  
     (Explanation: 11 = 5 + 5 + 1)

